#ifndef VERTEX_H
#define VERTEX_H
using namespace std;
/**

@brief Vertex class representing a point in 2D space.

A Vertex is a point in 2D space with an x-coordinate and a y-coordinate.

This class provides public access to the x and y coordinates.
*/
class Vertex {
public:
  //! x-coordinate of the Vertex.
  double x;
  //! y-coordinate of the Vertex.
  double y;
};
#endif